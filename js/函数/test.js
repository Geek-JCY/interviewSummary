/*
函数的应用大体上分为 以下四类：

    函数声明

    函数表达式

    匿名函数

    自执行函数
 */
/*
1.函数声明
    注意点：函数声明提升

2.函数表达式
    注意的点：函数表达式 是 变量声明的方式定义，也会 变量提升

3.匿名函数
    指没有显示进行声明赋值操作的函数，多作为参数使用；
    当匿名函数作为参数传递给另一个函数的时候，这个匿名函数最终会另一个函数中执行，所以我们也成其为回调函数；

4.自执行函数
    多用于创建模块 和 结合闭包模仿块级作用域；

    模块一般包括：私有变量，私有方法，公有变量，公有方法

 */